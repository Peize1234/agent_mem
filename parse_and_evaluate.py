def compose_answer(
    direct: str,
    topic: str,
    context: str,
    next_step: str,
    variant: int,
) -> str:
    """Generate a 1000–2000-char answer using only topic-specific material."""
    blocks = TOPIC_PACKS[topic]
    rotated = [blocks[(variant + i) % len(blocks)] for i in range(len(blocks))]
    parts = [
        f"直接回答：{direct}",
        f"结合你当前这一步的情况：{context}",
        rotated[0],
        rotated[1],
        rotated[2],
        rotated[3],
        f"下一步建议：{next_step}",
    ]
    text = "\n\n".join(parts)
    # Add the fifth topic block only if needed. It is still directly on topic.
    if len(text) < 1050:
        parts.insert(-1, rotated[4])
        text = "\n\n".join(parts)
    if not (1000 <= len(text) <= 2000):
        raise ValueError(f"Assistant answer length {len(text)} outside range for topic {topic}")
    return text


